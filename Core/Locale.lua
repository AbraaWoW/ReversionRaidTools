-- ============================================================
-- L table — lazy lookup with language fallback
-- ============================================================

local function GetLang()
    return (RRT and RRT.Settings and RRT.Settings.Language) or
           (GetLocale() == "frFR" and "FR" or "EN")
end

RRT_NS.L = setmetatable({}, {
    __index = function(_, k)
        local lang = GetLang()
        local t = lang == "FR" and FR or EN
        return t[k] or EN[k] or k
    end
})
